      <h3><!-- Tagline -->Bienvenido al mundo del totonacapan.</h3>
      <div id="bannerImage"><img src="estilos/torre.jpg" alt=""/></div>
      <p>Del náhuatl. Atl, a; agua, Tsalan; en medio, entre. "Entre aguas", alude a los arroyos que rodean a su actual cabecera.</p>
		
		<p>Sobre fondo argen (blanco) se destaca en forma exabada con un contorno azul en el cual se encuentra en la parte superior una estrella amarilla de cinco puntos, un batracio en cada lado denominado calate y la leyenda Atzalan.

		El campus está dividido en dos por una línea horizontal, en la pieza principal se encuentra un paisaje con cuatro cerros, destacando al centro la vieja torre solitaria del campanario y a su lado una casa. En la pieza secundaria sobre un fondo café, se encuentra el glifo que describe a Atzalan.

		Este escudo se encuentra coronado por dos ramas de cafeto en producción de cada lado, al centro se encuentra un libro abierto cruzado por una pluma y con la inscripción MDXIX, , sobresaliendo en la parte superior una antorcha encendida.</p>
		
		<p>En la época prehispánica fue una aldea totonaca de la Confederación de Mexicaltzingo. Altotonga, hoy municipio, llego a pertenecer a Atzalan. En 1569, siendo vicario de la provincia de Atzalán Fray Diego de López, se construyó la capilla de San Miguel y en 1670 el templo principal.

		Lo que ahora es la villa de Atzalan, fué fundada por Mexcaltecutli señor del Mezcalzingo hacia el año 1200. La fundación de Atzalan, se debió a motivos militares, pues se había dado la penetración nahua, principalmente, en Xalacingo y Nautla. El maestro David Ramirez Lavoignet en su libro Tlapacoyan establece: que el pueblo de Atzalan, originalmente pueblo confederado, queda convertido en aldea a raíz de la Conquista, estableciéndose aqui la nueva cabecera del señorío. En 1554 lo visita Diego Huitziquitzin a nombre del Virrey, dando en él posesión del cacicazgo a Don Pedro Fernández Pimentel, descendiente del señor del Mezcalcingo.</p>
		
		<p>Durante la conquista española, Atzalan fué de los primeros pueblos que recibieron la influencia del conquistador, el padre Francisco María Cortés, en su monografía de la parroquia de San Andrés Atzalan, establece que en el año 1639 se construyó la primera iglesia de este pueblo, la torre solitaria del campanario se construyó en el año de 1702.

		En el siglo pasado, en los años ochentas, siendo jefe político del cantón de Jalacingo Don Agustín Romo, se trasladó al pueblo de Atzalan los poderes cantonales o sea el juzgado de primera instancia, la agencia del ministerio público, el registro público de la propiedad., el reclusorio y la guardia de rurales; en esta época se inició la construcción del palacio municipal sobre lo que se conocia, desde la época de la colonia, como casas reales, la cual fue terminada en 1901.

		Los poderes cantonales duraron en Atzalan dos años, debido a la influencia de la familia Villegas asentada en Jalacingo éstos retornaron a esa población. Durante este tiempo, el pueblo de Atzalan tuvo un resurgimiento económico por encima de las poblaciones aledañas, así el primero de diciembre de 1891, el pueblo de Atzalan fué elevado a la categoria de villa, el cual todavía sustenta; para recordar este hecho, existe una placa conmemorativa que se encuentra incrustada en el kiosco del parque Colón.

		Con el advenimiento de la revolución los capitales que había en Atzalan desaparecieron, lo que la aletargó. Durante la época revolucionaria tocó a Atzalan tener el honor de haber sido testigo de la primera restitución de tierras hecha legalmente, es decir, que una vez promulgada la ley del 6 de enero de 1915, las comunidades de La Florida, Sompázol, Tatzallanala, Ictzictic y Xontaxpan recobraron las tierras de las cuales habían sido despojados y que detentaba el licenciado Benigno Ríos, propietario de la hacienda de Santa Cruz; esta restitución fué hecha por el teniente de armas</p>
		
